void probel(int shag)
{
	int i = 1;
	while (i <= shag)
	{
		printf(" ");
		i++;
	}
}
float v(float d, float w, float l)
{
	return (d*w*l);
}
float massa(float p0, float v)
{
	return(p0*v);
}
// ШКАФ
void polka(int j, int kol_n)
{

	switch (j % 8)
	{
	case 0:
	{
		printf("/");
		for (int k = 2; k <= kol_n; k++)
		{
			printf("_");
		}
		break;
	}
	case 7:
	{
		printf(" /");
		probel(kol_n - 2);
		break;
	}
	case 6:
	{
		printf("  /");
		probel(kol_n - 3);
		break;
	}
	case 5:
	{
		printf("   /");
		probel(kol_n - 4);
		break;
	}
	case 4:
	{
		printf("    /");
		probel(kol_n - 5);
		break;
	}
	case 3:
	{
		printf("     /");
		probel(kol_n - 6);
		break;
	}
	case 2:
	{
		printf("      /");
		probel(kol_n - 7);
		break;
	}
	case 1:
	{
		printf("       /");
		probel(kol_n - 8);
		break;
	}
	}

}

